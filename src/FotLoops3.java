public class FotLoops3 {
    public static void main(String[] args) {

        int num=2;
        while (num<10){
            System.out.println(num);
            num++;
        }

        for (int i=0;i<20;i++){
            if (i%3!=0){
                System.out.println(i);
            }
        }
    }
}
