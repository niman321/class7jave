public class Switch1Cup {
    public static void main(String[] args) {
        int worldCub=2022;
        switch (worldCub){
            case 2022:
                System.out.println("argintina");
                break;
            case 2018:
                System.out.println("france");
                break;
            case 2014:
                System.out.println("germany");
                break;
            default:
                System.out.println("persia");
        }
    }
}
